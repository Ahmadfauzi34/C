//! V8 Compiler Tiers and Inline Caches.
//!
//! This module simulates the multi-tier compilation strategy of V8,
//! moving from interpreted bytecode to highly optimized machine code.
//!
//! # Compilation Tiers
//! 1. **Ignition**: A register-based interpreter that executes bytecode.
//! 2. **Sparkplug**: A non-optimizing "fast" compiler that compiles bytecode 1-to-1.
//! 3. **Maglev**: A mid-tier optimizing compiler that performs basic SSA optimizations.
//! 4. **Turbofan**: The top-tier optimizing compiler that uses Sea-of-Nodes and advanced IR.

#[derive(Debug, Copy, Clone, PartialEq, Eq)]
pub enum ExecutionTier {
    Ignition,
    Sparkplug,
    Maglev,
    Turbofan,
}

/// Represents an Inline Cache (IC) site in the code.
///
/// ICs are critical for performance in dynamic languages. They store
/// information about object shapes encountered at specific bytecode offsets.
pub struct InlineCache {
    pub state: ICState,
    pub hits: u32,
    pub misses: u32,
    pub call_count: u32,
}

#[derive(Debug, Copy, Clone, PartialEq, Eq)]
pub enum ICState {
    Uninitialized,
    PreMonomorphic,
    Monomorphic,
    Polymorphic,
    Megamorphic,
    Generic,
}

impl InlineCache {
    pub fn new() -> Self {
        Self {
            state: ICState::Uninitialized,
            hits: 0,
            misses: 0,
            call_count: 0,
        }
    }

    pub fn record_hit(&mut self) {
        self.hits += 1;
        self.call_count += 1;
        if self.hits > 10 && self.state == ICState::Uninitialized {
            self.state = ICState::PreMonomorphic;
        } else if self.hits > 50 && self.state == ICState::PreMonomorphic {
            self.state = ICState::Monomorphic;
        }
    }

    pub fn record_miss(&mut self) {
        self.misses += 1;
        self.call_count += 1;
        if self.misses > 2 && self.state == ICState::Monomorphic {
            self.state = ICState::Polymorphic;
        } else if self.misses > 10 {
            self.state = ICState::Megamorphic;
        }
    }
}

