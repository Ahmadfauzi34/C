fn find_outer_closing_brace(content: &str, start: usize) -> Option<usize> {
    let mut depth = 1;
    for (i, c) in content[start..].char_indices() {
        if c == '{' { depth += 1; }
        else if c == '}' { depth -= 1; }
        if depth == 0 { return Some(start + i); }
    }
    None
}

fn export_enum(output: &mut String, name: &str, body: &str) {
    output.push_str(&format!("export enum {} {{\n", name));
    let mut val = 0;
    for line in body.lines() {
        let trimmed = line.trim();
        if trimmed.is_empty() || trimmed.starts_with('/') || trimmed.starts_with('#') { continue; }
        let variant_full = trimmed.split(',').next().unwrap_or("").trim();
        if variant_full.is_empty() { continue; }
        let variant_name = variant_full.split(['(', '{', '=']).next().unwrap_or("").trim();
        if !variant_name.is_empty() && variant_name.chars().next().unwrap_or(' ').is_uppercase() {
            output.push_str(&format!("  {} = {},\n", variant_name, val));
            val += 1;
        }
    }
    output.push_str("}\n\n");
}

fn export_struct(output: &mut String, name: &str, body: &str) {
    output.push_str(&format!("export interface {} {{\n", name));
    for line in body.lines() {
        let trimmed = line.trim();
        if !trimmed.starts_with("pub ") || trimmed.contains("fn ") { continue; }
        let field_part = trimmed[4..].split(',').next().unwrap_or("").trim();
        let parts: Vec<&str> = field_part.split(':').collect();
        if parts.len() == 2 {
            let f = parts[0].trim();
            let r = parts[1].trim();
            output.push_str(&format!("  readonly {}: {};\n", f, map_type(r)));
        }
    }
    output.push_str("}\n\n");
}

fn map_type(t: &str) -> String {
    let t = t.trim();
    if t == "usize" || t == "u32" || t == "u64" || t == "i32" || t == "f64" || t == "f32" { return "number".to_string(); }
    if t == "String" || t == "&'static str" { return "string".to_string(); }
    if t == "bool" { return "boolean".to_string(); }
    if t == "BrandedVAddr" || t == "TaggedAddress" || t == "BrandedPAddr" { return "bigint".to_string(); }
    if t == "MapIndex" || t == "ObjectIndex" || t == "BrandedTaskId" { return "number".to_string(); }
    if t.starts_with("Vec<") { return "ReadonlyArray<unknown>".to_string(); }
    if t.starts_with("HashMap<") { return "{ [key: string]: unknown }".to_string(); }
    "unknown".to_string()
}
