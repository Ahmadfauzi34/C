    generate_object_types(output)?;

    // Section 43-44: Compiler Pipeline
    generate_compiler_types(output)?;

    // Section 45: Sandbox
    generate_sandbox_types(output)?;

    // Section 46: WASM
    generate_wasm_types(output)?;

    // Section 47: Orinoco GC
    generate_gc_types(output)?;

