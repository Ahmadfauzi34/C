/// A simple scanner that finds an enum definition in a Rust file and exports it to TS.
fn scan_and_export_enum(output: &mut String, path: &str, enum_name: &str) -> std::io::Result<()> {
    let mut content = String::new();
    if let Ok(mut file) = File::open(path) {
        file.read_to_string(&mut content)?;

        if let Some(start) = content.find(&format!("pub enum {} {{", enum_name)) {
            let rest = &content[start..];
            if let Some(end) = rest.find('}') {
                let body = &rest[..end + 1];

                output.push_str(&format!("export enum {} {{\n", enum_name));

                let mut value_counter = 0;
                for line in body.lines() {
                    let trimmed = line.trim();
                    if trimmed.is_empty() || trimmed.starts_with("pub enum") || trimmed.starts_with('}') || trimmed.starts_with('/') || trimmed.starts_with('#') {
                        continue;
                    }

                    let variant = trimmed.split(',').next().unwrap_or("").split('=').next().unwrap_or("").trim();
                    if !variant.is_empty() {
                        output.push_str(&format!("  {} = {},\n", variant, value_counter));
                        value_counter += 1;
                    }
                }
                output.push_str("}\n\n");
            }
        }
    }
    Ok(())
}

/// A scanner that finds a struct definition and exports it as a TS interface.
fn scan_and_export_struct(output: &mut String, path: &str, struct_name: &str) -> std::io::Result<()> {
    let mut content = String::new();
    if let Ok(mut file) = File::open(path) {
        file.read_to_string(&mut content)?;

        if let Some(start) = content.find(&format!("pub struct {} {{", struct_name)) {
            let rest = &content[start..];
            if let Some(end) = rest.find('}') {
                let body = &rest[..end + 1];

                output.push_str(&format!("export interface {} {{\n", struct_name));

                for line in body.lines() {
                    let trimmed = line.trim();
                    if !trimmed.starts_with("pub ") || trimmed.contains("struct ") || trimmed.contains('}') {
                        continue;
                    }

                    let parts: Vec<&str> = trimmed.split(':').collect();
                    if parts.len() == 2 {
                        let field = parts[0].trim_start_matches("pub ").trim();
                        let rust_type = parts[1].trim_end_matches(',').trim();

                        // Handle diagnostic report failures field mapping
                        if field == "failures" && rust_type == "Vec<FailureKind>" {
                            output.push_str("  readonly failures: ReadonlyArray<FailureContext>;\n");
                            continue;
                        }

                        let ts_type = match rust_type {
                            "usize" | "u32" | "u64" | "i32" | "f64" => "number",
                            "String" | "&'static str" => "string",
                            "bool" | "boolean" => "boolean",
                            "Vec<u64>" => "ReadonlyArray<number>",
                            _ => "any",
                        };
                        output.push_str(&format!("  readonly {}: {};\n", field, ts_type));
                    }
                }
                output.push_str("}\n\n");
            }
        }
    }
    Ok(())
}
